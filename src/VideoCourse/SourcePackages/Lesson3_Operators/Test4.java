package VideoCourse.SourcePackages.Lesson3_Operators;

public class Test4 {
    public static void main(String[] args) {
        int a = 11;
        int b = 3;

        System.out.println(a + b);

        int c = a + b;
        System.out.println(c);

        int d = b - a;
        System.out.println(d);
        System.out.println(a * b);

        int e = a / b;
        System.out.println(e);
        System.out.println(a / b);

        System.out.println(a % b);
    }
}
